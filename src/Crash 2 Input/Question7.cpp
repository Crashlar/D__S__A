#include <iostream>
using namespace std;

int main(){
    // πr²h
    float radius , height;
    cout << "Enter the radius : ";
    cin >> radius;

    cout << "Enter the height : ";
    cin >> height;
    // calculating volume 
    cout << "volume of cylinder :"<< 3.1416 * radius * radius * height << "unit cube";
    return 0;
}



